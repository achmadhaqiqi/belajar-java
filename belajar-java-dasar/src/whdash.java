public class whdash {
    public static void main(String[] args) {

    // Contoh 1: Loop untuk iterasi sebanyak 5 kali
    for (int i = 0; i < 5; i++) {
        System.out.println("Iterasi ke-" + (i + 1));
    }

    System.out.println(); // Membuat baris kosong

    // Contoh 2: Loop untuk iterasi dari 1 hingga 10
    for (int j = 1; j <= 10; j++) {
        System.out.print(j + " ");
    }

    }
}
